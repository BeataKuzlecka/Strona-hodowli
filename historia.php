jej córkę: <li><i>Odette Kudłaty Kłębuszek ur. 19 lipca 2013r.</i></li>

oraz <li><i>Dolly Kudłaty Kłębuszek ur. 4 czerwca 2007r.</i> - od Meli od Pana Waldemara</li>
</ul></p>
