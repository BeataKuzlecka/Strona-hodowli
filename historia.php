<h2>Hodowla Kudłaty Kłębuszek to niezły kawałek historii, zapraszamy do zapoznania się z jej częścią.</h2>

<p>Nasza przygoda z pudlami morelowymi zaczęła się przypadkiem, ponieważ sąsiadka, która miała reproduktora Amora Piasek Pustyni namówiła nas na kupno szczeniaka i założenia hodowli.
W styczniu 1984r. zjawiła się w domu morelowa pudliczka Dinka Arabrab z hodowli w Toruniu.

</br><img src="images/Dinka.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Dinka Arabrab 18 listopada 1983r. - 19 marca 1993r.</i></br>

Pod koniec 1985 r. urodziły się pierwsze szczeniaczki, które wyglądały jak kudłate kłębuszki i stąd wzięła się nazwa hodowli.
Jedno ze szczeniąt zostawiliśmy i nazwaliśmy Anetką.

</br><img src="images/Anetka.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Anetka Kudłaty Kłębuszek 12 listopada 1985r. - 19 marca 1993r.</i></br>

Gdy wyhodowaliśmy sporą ilość szczeniąt i suczki zaczęły się starzeć, nadszedł czas, by kto inny zaczął pchać do przodu naszą hodowlę. Tak więc od Dinki zostało kolejne kudłate szczenię. Jessy.
</br><img src="images/Jessystar.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Jessy Kudłaty Kłębuszek 24 grudnia 1991r. - 31 lipca 2003r.</i></br>

W 1991 r. przybyła do naszego domu czarna pudliczka 2.5 letnia Blanka z Ziębiej, która zapoczątkowała hodowlę czarnych pudli.
</br>
<img src="images/Blanka.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Blanka z Ziębiej 9 maja 1989r. - 4 kwietnia 2002r.</i></br>

Po kilku latach kiedy niestety odeszły od nas Dinka i Anetka, żeby zapełnić pustkę została z nami Nuka.
</br>
<img src="images/Nuka.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Nuka Kudłaty Kłębuszek 24 czerwca 1994r. - 25 kwietnia 2002r.</i></br>

W 2003 roku, gdy została z nami już tylko Jessy, doszliśmy do wniosku że przydały się towarzysz dla naszej staruszki. Niestety Jessy była już w zbyt podeszłym wieku, aby mieć młode. Tak więc zakupiona została młoda pudliczka od zaprzyjaźnionej hodowli "Jantarowa Fuga". Zrządzeniem losu okazało się, że maleństwo ma takie samo imię jak nasza samotniczka.

</br><img src="images/12030008.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Jessy Jantarowa Fuga ur. 12 marca 2003r.</i></br>

Starsza Jessy była mentorem dla swej małej imienniczki.</br>
<img src="images/12030006.jpg" style=" margin: 10px 10px 10px 0px;"/></br>
Długo nie pocieszyły się swoją obecnością, po kilku miesiącach opuściała nas starsza Jessy.
Mieliśmy jedno szczenię, postanowiliśmy dokupić mu czarną pudliczkę, Melę.</br>

<img src="images/05260319.jpg" style=" margin: 10px 10px 10px 0px;"/><i>Mela od Pana Waldemara ur. 28 sierpnia 2004r.</i></br>

W chwili obecnej oprócz wymienionej moreli i czarnuszki, mamy:

